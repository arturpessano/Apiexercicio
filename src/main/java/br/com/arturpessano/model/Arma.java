package br.com.arturpessano.model;

public class Arma {

	public String dano;
	public String calibre;
	public String nomedaarma;
	
	public Arma(String nomedaarma,String calibre,String dano) {
		super();
		this.dano = dano;
		this.calibre = calibre;
		this.nomedaarma = nomedaarma;
	}



	public String getDano() {
		return dano;
	}



	public void setDano(String dano) {
		this.dano = dano;
	}



	public String getCalibre() {
		return calibre;
	}



	public void setCalibre(String calibre) {
		this.calibre = calibre;
	}



	public String getNomedaarma() {
		return nomedaarma;
	}



	public void setNomedaarma(String nomedaarma) {
		this.nomedaarma = nomedaarma;
	}
	
	
	
	
	
	
	

	
	}
	

	
	

	
	

	
	
	

	

	