package br.com.arturpessano.model;

public class Mapa {

	public String tempodasafe;
	public String duracaodapartida;
	
	
	public Mapa(String tempodasafe, String duracaodapartida) {
		super();
		this.tempodasafe = tempodasafe;
		this.duracaodapartida = duracaodapartida;
	}


	public String getTempodasafe() {
		return tempodasafe;
	}


	public void setTempodasafe(String tempodasafe) {
		this.tempodasafe = tempodasafe;
	}


	public String getDuracaodapartida() {
		return duracaodapartida;
	}


	public void setDuracaodapartida(String duracaodapartida) {
		this.duracaodapartida = duracaodapartida;
	}
	
	
	
	
	
	
}
